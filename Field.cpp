// フィールドの初期化処理
void InitField(void) {
	entity.X = 0; // 横軸
	entity.Y = 0; // 縦軸

	entity.key = 0;
	// プレイヤーの初期化
	InitPlayer();

	// エネミーの初期化
	InitEnemy();
	
}

// フィールドの終了処理
void UninitField(void) {
	UninitPlayer();
	UninitEnemy();
}

// フィールド表示処理
void DrawField(void) {
	PLAYER* player = GetPlayer();
	ENEMY* enemy = GetEnemy();

	// TODO: PVE Collision & Change State... FIELD -> BATTLE

	for (int y = player->y - m_uPOV; y < player->y + m_dPOV + entity.Y; y++) {
		// 1行表示
		for (int x = player->x - m_lPOV; x < player->x + m_rPOV + entity.X; x++) {
			if (y < 0 || y >= m_HEIGHT || x < 0 || x >= m_WIDTH) {
				printf("X");
			}
			// Y行目のX列目がプレイヤーの座標と一致したら
			else if (x == player->x && y == player->y) {
				// Pを表示
				DrawPlayer();
				
			}
			else if ((x == enemy->x && y == enemy->y))
			{
				DrawEnemy();
			}
			else {
				// 配列の中身にしたがって表示する
				switch (field[y][x]) {
				case 0: // 通れる
					printf(" ");
					break;
				case 1: // 障害物
					printf("#");
					break;
				case 2: // ライド
					printf("T");
					break;
				case 3: // キー
					printf("K");
					break;
				case 4: // ドア
					printf("D");
					break;
				case 5: // ゴール
					printf("G");
					break;
				default: // エラー検知
					printf("$");
					break;
				}
			}
		}
		printf("\n"); // 次の行へ改行する
	}

	printf("\n\n　#: 壁	\n　T: ライド\n　K: 鍵\n　D: 鍵がかかった扉\n\n\n\tKey:\t %d\n\t%s\tHP: %d",entity.key, player[0].name, player[0].HP);

	switch(player[0].status)
	{
		case ERROR_NO:
			break;
		case ERROR_KEY:
			printf("\n鍵が掛かっている、どこに鍵があるはず、、、\n");
			Sleep(1000);
			player[0].status = ERROR_NO;
			break;
		
	}
	
	Sleep(100);
}

