// タイトルの更新処理
void UpdateTitle(void) {
	//　アニメーションカウンターを進める
	

	if (_kbhit() == 0) {
		system("cls");
		return;
	}

	// キー入力を取得する
	rewind(stdin);
	int key = _getch();
	if (key == 0 || key == 0xe0) {
		key = _getch(); // 戻り値：ASCIIコード
	}
	system("cls");
	
	// モード切り替え
	SetMode(GAME_FIELD); // フィールドへ遷移

}
// タイトルの描画処理
void DrawTitle(void) {

	for (int y = 0; y < t_HEIGHT; y++) {
		printf("　　　　　　");
		// 1行表示
		for (int x = 0; x < t_WIDTH; x++) {
			// Y行目のX列目がプレイヤーの座標と一致したら

				// 配列の中身にしたがって表示する
			switch (title[y][x]) {
			case 0: // 通れる
				printf(" ");
				break;
			case 1: // 障害物
				printf("#");
				break;
			case 2: // ライド
				printf("T");
				break;
			case 3: // キー
				printf("K");
				break;
			case 4: // ドア
				printf("D");
				break;
			case 5: // ゴール
				printf("G");
				break;
			default: // エラー検知
				printf("$");
				break;
			}
		}
		printf("\n"); // 次の行へ改行する
	}
	Sleep(200);
}




