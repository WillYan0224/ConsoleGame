// タイトルの描画処理
void DrawTitle(void) {
	PLAYER* player = GetPlayer();
	printf("\n     ");
	for (int y = 0; y < t_HEIGHT; y++) {
		// 1行表示
		for (int x = 0; x < player->x + t_rPOV + camera.X; x++) {
			if (x == player->x && y == player->y) {
				// Pを表示
				DrawPlayer();
			}
			else
			{
				switch (title[y][x]) {
				case 0: // 通れる
					printf(" ");
					break;
				case 1: // 床
					printf("=");
					break;
				case 2: // ヒント1
					printf("|");
					break;
				case 3: // ヒント2
					printf("v");
					break;
				case 4: // 下層床
					printf("X");
					break;
				case 5: // FOV拡大
					printf("-");
					break;
				default: // エラー検知
					printf("$");
					break;
				}
				
			}
		}
		printf("\n"); // 次の行へ改行する
		printf("     ");
		
	}
	Sleep(200);
}



