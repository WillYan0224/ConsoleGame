//タイトル
//　タイトルの初期化
void InitTitle(void) {
	titleCnt = 0; // タイトルループをクリア
}
// タイトルの終了処理
void UninitTitle(void) {

}
// タイトルの更新処理
void UpdateTitle(void) {
	//　アニメーションカウンターを進める
	titleCnt = (titleCnt + 1) % ANIM_MAX; // 0 1 2 3 4		5->0

	if (_kbhit() == 0) {
		system("cls");
		return;
	}

	// キー入力を取得する
	rewind(stdin);
	int key = _getch();
	if (key == 0 || key == 0xe0) {
		key = _getch(); // 戻り値：ASCIIコード
	}
	system("cls");
	
	// モード切り替え
	SetMode(GAME_FIELD); // フィールドへ遷移

}
// タイトルの描画処理
void DrawTitle(void) {
	for (int y = 0; y < DISP_H; y++) {
		printf("%s", &title[y + titleCnt * DISP_H][0]);
	}
	printf("press any key");
	Sleep(300);
}




