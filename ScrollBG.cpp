// フィールド表示処理
void DrawScrollBG(void) {
	PLAYER* player = GetPlayer();
	ENEMY* enemy = GetEnemy();
		printf("\n     ");
		for (int y = player->y - bg_uPOV; y < player->y + bg_dPOV; y++) {
			// 1行表示
			for (int x = player->x - bg_lPOV; x < player->x + bg_rPOV + light.X; x++) {
				if (y < 0 || y >= bg_HEIGHT || x < 0 || x >= bg_WIDTH) {
					printf(" ");
				}
				// Y行目のX列目がプレイヤーの座標と一致したら
				else if (x == player->x && y == player->y) {
					// Pを表示
					DrawPlayer();

				}
				else if ((x == enemy->x && y == enemy->y))
				{
					DrawEnemy();
				}
				else {
					// 配列の中身にしたがって表示する
					switch (scrollBG[y][x]) {
					case 0: // 通れる
						printf(" ");
						break;
					case 1: // 障害物
						printf("#");
						break;
					case 2: // ライド
						printf("T");
						break;
					case 3: // キー
						printf("K");
						break;
					case 4: // ドア
						printf("D");
						break;
					case 5: // ゴール
						printf("G");
						break;
					default: // エラー検知
						printf("$");
						break;
					}
				}
			}
			printf("\n     ");
		}
		Sleep(100);
	}


